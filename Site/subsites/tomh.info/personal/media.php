<h2>Radio radio</h2>
<p>
After witnessing the regrettable demise of
<a href="http://www.flagpole.com/Issues/02.24.99/ort.html"
    onMouseOver="linkStatus();return true;"
    onMouseOut="setStatusToDefault();return true;"
    target="radioWindow">WGOE</a>
in Richmond, Virginia,
for decades I refused to listen to the radio.
I know what music I want to listen to and when, and
moreover why put up with commercials?
</p>
<p>
Since moving to Denver, however, I've found a couple of
commercial-free radio stations worth listening to:
</p>
<ul>
<li>All of my radios are set to
<a href="http://www.kuvo.org/"
    onMouseOver="linkStatus();return true;"
    onMouseOut="setStatusToDefault();return true;"
    target="radioWindow">KUVO</a>
at 89.3 FM.
Jazz enthusiasts and those who listen to KUVO should be pleased to
know that Bob Parlocha now has an 
<a href="http://www.jazzwithbobparlocha.com/"
    onMouseOver="linkStatus();return true;"
    onMouseOut="setStatusToDefault();return true;"
    target="radioWindow">extensive website</a>.
KUVO streams on the web so you can listen to it wherever you live.</li>
<li>When I have the time and feel like getting away from it all,
I often go up to the 
<a href="http://www.mapquest.com/maps/map.adp?country=US&addtohistory=&address=&city=poudre&state=CO&zipcode=&homesubmit.x=31&homesubmit.y=12"
    onMouseOver="linkStatus();return true;"
    onMouseOut="setStatusToDefault();return true;"
    target="radioWindow">Cache Poudre River</a>,
which is north west of Fort Collins.
This is due in part to the excellent college radio station they
have up there,
<a href="http://www.kcsufm.com/"
    onMouseOver="linkStatus();return true;"
    onMouseOut="setStatusToDefault();return true;"
    target="radioWindow">KCSU</a>.
If you're ever up in those parts, check them out at 90.5
(a mere two clicks of the search button above KUVO).</li>
</ul>
<p>
Thanks to KUVO and KCSU, sometimes I feel like maybe I'm not
"on the wrong planet" after all.
</p>


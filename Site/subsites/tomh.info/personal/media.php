<h1>Media</h1>
<p>
After reading some books about the history of dance,
recently I started buying some dance videos.
</p>
<p>
Watching modern dances created by Denishawn, Isadora Duncan, Alvin Ailey,
Doris Humphrey, Martha Graham, Jos&eacute; Lim&oacute;n, and Merce Cunningham,
and performed by their dance companies, is really neat.
And believe it or not, after studying the history of ballet,
I even enjoy watching that.
Hey, <em>all</em> those dancers are <em>so</em> beautiful.
</p>
<p>
You should try it sometime: give dance a chance.
</p>

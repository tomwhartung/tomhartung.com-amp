<h2>Imagine turning the tables</h2>
<p>
What if the history tables were turned?
That is, what if high school history teachers, instead of teaching
the history of politics and war, taught the history of art and science?
And I mean <em>all</em> arts and <em>all</em> sciences!
</p>
<p>
As someone who's lived through a few wars, it's obvious to me that
art and technology reflect the realities of living in a society
at least as much, if not more so, than governments and conflicts.
Moreover, all these things go hand in hand.
</p>
<h3>Shifting the focus</h3>
<p>
Shifting the focus to heroes in the evolution of communication and
expression would still allow students to learn plenty about
governments and conflicts.
But imagine if children were taught about the people
who move Wassily Kandinsky's
<a href="http://www.globusz.com/ebooks/Kandinsky/00000013.htm"
  onMouseOver="linkStatus();return true;"
  onMouseOut="setStatusToDefault();return true;"
  target="otherWindow">triangle</a>
and
<a href="http://www.globusz.com/ebooks/Kandinsky/00000015.htm"
  onMouseOver="linkStatus();return true;"
  onMouseOut="setStatusToDefault();return true;"
  target="otherWindow">pyramid</a>,
rather than those who contribute to and directly cause tragedy,
death, and destruction.
Do you honestly think things would be <q>better</q> or <q>worse?</q>
</p>
<p>
Of course the history of politics and war would be specialty classes
that interested students could take in college, much the way
art history and science are taught today.
I'm not talking about any sort of censorship, just a shift of focus
to healthier, more positive material, at the age when kids are
trying to figure out what they want to do when they grow up.
</p>
<h3>Continual progress vs. continual change</h3>
<p>
Politicians often rationalize various nanny-state laws, such as
those concerning seat belts, smoking tobacco, and
keeping certain drugs illegal, by stating that these compromises
of our freedoms are justified if we can save just one life.
</p>
<p>
How many lives might be saved if we shifted the focus of the
teaching of history as described above?
How might this affect our daily lives?
Would this also shift the focus of research 
on new technologies and the content of news programs?
</p>
<p>
How many lives might be saved if the length of a war was 
shorted by a day or a week or a month, or avoided altogether?
</p>
<p>

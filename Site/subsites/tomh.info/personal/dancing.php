<h3>How dancing inspired a new type of jazz</h3>
<p>
Currently I'm reading <em>Miles: The Autobiography</em>, and
it is extremely cool to learn that he was married to a dancer
named
<a href="http://images.amazon.com/images/P/B00000DCH2.01.LZZZZZZZ.gif"
         onMouseOver="linkStatus();return true;"
         onMouseOut="setStatusToDefault();return true;"
         target="dance_window">
Francis Taylor</a>.
In the quote below, "the modal thing" refers to the type of jazz
he pioneered on the album
<a href="http://www.amazon.com/exec/obidos/ASIN/B000002ADT/qid=1050379112/sr=2-1/ref=sr_2_1/002-3809842-3000867"
         onMouseOver="linkStatus();return true;"
         onMouseOut="setStatusToDefault();return true;"

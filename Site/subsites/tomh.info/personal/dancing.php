<h3>How I got interested in dancing</h3>
<p>
It all started one day in February, 1997, when
I got a coupon in the mail for 20% off on dance lessons at a local
ballroom dancing studio named <em>The World of Dance</em>.
It sounded like something different, possibly fun, and,
according to conventional wisdom, a good way to meet women.
I cogitated on the coupon for a week or two and, being bored with my
life at that time (did I mention it was in February?),
finally used the coupon a few days before it expired.
</p>
<p>
Over the next four years I took private lessons and
all sorts of classes at The World of Dance, and
learned swing (jitterbug and lindy hop),
salsa, cha-cha, rumba, waltz, samba, merengue, fox-trot, tango, and even
a little polka.
When the teachers I had there retired, I started taking lessons
and classes at
<a href="http://www.karenleedance.com"
  onMouseOver="linkStatus();return true;"
  onMouseOut="setStatusToDefault();return true;"
  target="dance_window"> 
  Karen Lee Dance International</a> (KLDI).
</p>

<h3>Looking for the music theory of dancing</h3>
<p>
By 2001, I had a solid background in the basics of partner dancing,
but still had difficulty in the more advanced classes.
It was frustrating to spend $100 in a weekend to take classes from
a visiting instructor like Frankie Manning, and be able to
remember only maybe about 10-25% of what I learned.
</p>
<p>
It was about this time I was wondering, "what is the music theory of dancing?
What can I learn that will be like learning <em>all of dancing</em>?"
What movements are the alphabet and vocabulary of dancing?
Where can I get <em>education</em> instead of <em>training</em>?
</p>
<p>
From dancing with beginners, I learned that women who have
taken ballet were much easier to lead than those who hadn't.
Also, the teachers I had for private lessons had taken jazz, ballet, tap,
and other performance dance classes.
After hearing me complain about my lack of memory
("my brain is full!") when trying to recall recently-learned moves,
some of my dance partners suggested I take some classes at a place like
<a href="http://www.cleoparkerdance.org"
         onMouseOver="linkStatus();return true;"
         onMouseOut="setStatusToDefault();return true;"
         target="dance_window">
Cleo Parker Robinson's Dance School (CPRD)</a> (CPRD).
</p>

<h3>A mini-Juilliard right in my back yard!</h3>
<p>
As it happens, CPRD is about two blocks from where I live.
And in June of 2001, I found myself unemployed, with enough
savings to get by on, but definitely not enough to continue with the
private lessons and classes in partner dancing.
So in July, 2001 I mustered up the courage to take a
Beginning Jazz dance class at CPRD.
It was huge fun and a great work-out, and
I felt totally liberated, finally for once being able
to dance without having to worry about leading!
</p>
<p>
As the summer of 2001 came to an end, Hip-hop and Salsa classes came next,
and as the economy and World Trade Center Towers went their way,
so did my hopes of finding employment that fall anyway, and
for who knows how long after that.
</p>
<p>
Just as I was in the bluegrass band,
(or just about anyone else in any bluegrass, jazz, punk rock, etc.
band for that matter)

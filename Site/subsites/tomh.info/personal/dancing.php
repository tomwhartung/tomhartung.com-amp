<h2>Tomh's saga of dancing in Denver</h2>
<p>
When I was young, growing up in the suburbs of
Richmond, Virginia, people just didn't dance.
At least, no one I knew did.
It was the age of The Beatles, CSNY, The Dead, Who, Byrds, Kinks, Doors,
Stones, Jimi Hendrix, Jefferson Airplane, etc.
Everyone wanted to be a Rock 'n' Roll star.
</p>

<h3>Learning about music and learning about learning</h3>
<p>
I played trumpet from sixth grade through high school, and
in my senior year was in a rock band with some classmates.
We had a full ensemble, but couldn't find a reliable bass player.
</p>
<p>
Also in my senior year I took a class in music theory and solfeggio
(ear and voice training).
Prior to this I'd learned only <em>specific songs</em>,
this was like learning <em>all of music</em>.
Maybe it was the private lessons or maybe it was
having the discipline to do the scales and
arpeggios more than knowing the theory
(undoubtedly it was all of the above) but
about this time I began to improvise on the horn, and
jam along with Freddy Hubbard and Miles Davis records.
<!-- (It always sounded best when no one was around!) --!>
</p>
<p>
Back in those days, if you wanted to be a rock 'n' roll star,
trumpet was obviously the wrong way to go.
So after graduation from Hell School, err, High School,
I bought a used Framus hollow-body electric bass for $50.
Thanks I am sure to working with  solfeggio,
it was surprisingly easy to learn and
play Paul McCartney's simple yet <em>incredibly beautiful</em>
bass lines.
<!-- I listened to the Beatles so much in those days, I
can't hardly stand to hear them anymore! --!>
Soon ex-trumpeter Phil Lesh (bass player for the Dead) became
my main source of inspiration.
Later I used what I learned in that same music theory
class to teach myself guitar.
</p>
<h3>Using the knowledge and always learning more</h3>
<p>
Some years later I got a gig playing stand-up bass in a bluegrass band.
I was still young and had a bit of an attitude.
The deal was, the money didn't matter much,
<!-- (I made plenty of that programming in Cobol) --!>
what was important was that sometime during each set
I'd get to play guitar and sing one of my "hippie songs," like
<em>Me and My Uncle</em>, <em>Sweet Virginia</em>,
<em>You Ain't Goin' Nowhere</em>, and <em>Friend of the Devil</em>.
</p>
<p>
The bluegrass band was fine for a few years until I started
getting into Punk Rock.
Somehow it seemed that doing a Clash song, even one from the laid-back
album <em>Sandinista</em>, in a bluegrass style just wouldn't work....
</p>
<p>
That music theory class also wound up having a profound effect on
how I learned photography and dancing.
Pursuing an interest in photography, I wound up taking a lot of
art history and even a drawing class.
Eventually I found the "music theory of visual arts" in
writings by Mondrian and Kandinsky.
I remember specifically finding Kandinsky's book
<a href="http://store.doverpublications.com/0486238083.html"
  onMouseOver="linkStatus();return true;"
  onMouseOut="setStatusToDefault();return true;"
  target="dance_window"> 
Point and Line to Plane</A> to be fascinating.
<!-- I wonder how many web designers have read it?
Well, that sort of abstract knowledge is difficult to understand
and it probably won't help you earn more money, so why bother? --!>
<!-- That education probably explains why I like
Film Noir more than many people. --!>
</p>
<p>
Soon after this, in Graduate School, I studied Artificial Intelligence.
At this time I became consciously aware of the difference between
being smart vs. being intelligent, and why training is cheaper
and easier than education.
Like, duh!
Obviously in these changing times, <em>education</em> is the way to go!
</p>
<p>
Please don't hate me because I get bored easily!
I write about doing all this stuff, but I don't do any of
it very well.
I just get bored with everything.
Lately it's become obvious that I enjoy the learning part the most....
To do something well, you need to stick with it,
and actually that <em>is</em> something I <em>have</em> been doing recently,
which is why you should come to the
<a href="../personal/personal.php"
  onMouseOver="linkStatus();return true;"

<h2>Musicality</h2>
<p>
Recently I've been reading about the history of dance, and
have been buying some videos of modern dance in particular.
I like modern dance the best, and I believe it's for the same
reason I like jazz, the blues, bluegrass, hip-hop, and film noir:
because all of these are uniquely American art forms.
</p>
<h3>Isadora Duncan 101</h3>
<p>
So far the dancer who impresses me the most is Isadora Duncan (1877-1927).
Over the years, a few of my teachers have mentioned <q>musicality,</q>
but I had no clue what that term meant until I saw a video of
one of Isadora's dances.
</p>
<p>
During Isadora's time, someone who wanted to dance would have to study ballet.
Although she loved classical music, she didn't care
for ballet's <q>affected grace and toe-walking,</q>
so she had to invent her own movements.
</p>
<p>
She would stand for hours, with her hands over her solar plexus,
<q>seeking that dance which might be the divine expression of
the human spirit through the medium of the body's movement.</q>
</p>
<p>
To quote further:
</p>
<div class="quote">
<blockquote>
After many months, when I had learned to concentrate all my force to
this one Centre, I found that thereafter when I listed to music the rays
and vibrations of the music streamed to this one fount of light within
me -- there they reflected themselves in Spiritual Vision, not the brain's
mirror, but the soul's, and from this vision I could express them in Dance.
</blockquote>
</div>
<p class="centered">
From <em>My Life</em>,
<br />
by
<a href="http://www.isadoraduncan.org/About_Isadora/about_isadora.html"
  onMouseOver="linkStatus();return true;"
  onMouseOut="setStatusToDefault();return true;"
  target="other_window">
Isadora Duncan</a>, 1927.
</p>
<p>
Ok, so you can take computer graphics classes in the Math department of
most any college, and learn how to put colored pixels on the screen.
But if, on the other hand, you take a computer graphics class in the Art department,
and the teacher makes you read Kandinsky's <q>Point and Line to Plane,</q>
you might learn how to <em>apply</em> that knowledge.
</p>
<p>
Similarly, you can take social and performance dance classes in
untold numbers of studios accross the land, and learn some movements that
will have some <q>built-in musicality</q> for a given genre of music.
But you will have to learn about and study someone like Isadora Duncan
to learn how to <em>apply that</em> knowledge.
</p>
<h3>Applying Isadora's principles</h3>
<p>
Before learning about Isadora, I had basically been learning movements.
Not having an interest in classical music,
and not really wanting to learn Isadora's movements,
<!-- (frankly, if one was to describe her style in
     a single a word, it could be called <q>frolicking</q>), -->
my path is clear.
</p>
<p>
What I need to do is apply Isadora's principles to my situation.
</p>
<h4>What's easy</h4>
<p>
Because I've been listening to rock and roll for many decades,
and don't have to worry about leading a partner,
this is incredibly easy for me to do at rock 'n' roll shows.
</p>
<p>
For example, for the post-Isadora-tomh, going to shows that feature
free-flowing, jazzy rock, like The Dead, will never be the same.
It's like I can feel a direct connection from the music to my body.
Another example is punk rock, a genre that expresses ample amounts
of emotion and, in some cases at least (e.g., Sonic Youth and
Tomahawk more so than The Melvins), plenty of synchopation.
</p>
<p>
It's also fairly easy for me to do this in the privacy of my own
home, where in addition to not having to worry about leading,
there is no one watching and making me feel self-conscious.
</p>
<h4>What's not so easy</h4>
<p>
Because I've been taking social dance classes for many years,
and have the obligation to lead a partner, this is extremely difficult
for me to do while dancing swing and salsa.
</p>
<p>
Obviously, my work is cut out for me.
I can feel the emotions, and can express them in movements in
some cases, but not all.
A little pain in a leg might keep me from learning other people's
movements, but it is not going to stop me from expressing
what's in my soul.
</p>
<p>
When doing something, it's important to have a goal....
</p>

<h3>The alphabet and vocabulary of dance</h3>
<p>
That's what they teach at CPRD: the alphabet and vocabulary of dance.
Occasionally a teacher will come out and say this,
but for the most part it's understood, it's why we're there.
Well that, and the endorphins, and the ethnic rhythms, and....
Taking classes there, I am constantly remembering how
powerful the lessons I learned in music theory were,
and how much fun it was taking Art and Art History classes at VCU.
</p>
<p>
The parallel now is, instead of learning <em>specific dances</em>,
I am learning <em>all of dancing</em>.
Since it came first, Ballet got dibs on naming some of the
most basic of these movements: tendu, plie, degage, etc.
Many of the other dances, especially Jazz and Modern,
add their own character to these basic movements.
An exception of course is West African,
which started an entirely different, less formal, branch.
It has its own vocabulary, but without the terms,
ya just gotta watch it and try it.
How Hip-Hop fits into this scheme is left as an exercise for the reader....
</p>
<p>
Despite Osama and various CEOs,
in May of 2002 I had a job interview
that led to an actual job in September.  Whoo-hoo!
Time off is nice but enough is enough,
the starving artist lifestyle (lots of MacCheese)
definitely gets old after awhile.
I tried going back to partner dancing, but
felt I needed more time before going back to the old
places -- the smoky Salsa bars in particular -- without the ciggies.
So in I decided keep my momentum going at CPRD at least through the showcase,
to continue my "basic training" and laying this foundation for partner dancing.
</p>


<em>Kind of Blue</em></a>.
</p>
<div class="quote">
<blockquote>
"I had gotten into the modal thing from watching a performance
by the Ballet Africaine from Guinea....
 Anyway, we went to this performance by the Ballet Africaine and
it just fucked me up what they was doing,
the steps and all them flying leaps and shit.... 
It was beautiful. 
And their rhythm! 
The rhythm of the dancers was something. 
I was counting off while I was watching them. 
They were so acrobatic.
They had one drummer watching them dance, doing their flips and shit,
and when they jumped he would play DA DA DA DA POW!
in this bad rhythm. 
He would hit it when they would fall. 
And man, he was catching everybody that did anything. 
The other drummers got them, too. 
So they would do rhythms like 5/4 and 6/8 and 4/4, and
the rhythm would be changing and popping. 
That's the thing, that secret, inner thing that they had. 
It's African. 
I knew I couldn't do it from just watching them dance because
I'm not African, but I loved what they were doing. 
I didn't want to copy that, but I got a concept from it."
</blockquote>
</div>
